//Add
function add(a, b) {
    return a + b
}

let resultAdd = add(1, 2)
console.log(resultAdd);

//Subtract
function subtract(a, b) {
    return a - b
};

let resultSubtract = subtract(3, 2);
console.log(resultSubtract);

//Multiply
function multiply(a, b) {
    return a * b
}
let resultMultiply = multiply(3, 2);
console.log(resultMultiply);

//Divide
function divide(a, b) {
    return a / b
}
let resultDivide = divide(10, 5);
console.log(resultDivide);